export const getScheduleStats = () => {
  const totalSessions = MOCK_SCHEDULE.length
  const totalEnrolled = MOCK_SCHEDULE.reduce((sum, session) => sum + session.enrolled, 0)
  const totalCapacity = MOCK_SCHEDULE.reduce((sum, session) => sum + session.capacity, 0)
  const enrollmentRate = Math.round((totalEnrolled / totalCapacity) * 100)
  const uniqueCourses = new Set(MOCK_SCHEDULE.map(session => session.courseCode)).size
  const uniqueProfessors = new Set(MOCK_SCHEDULE.map(session => session.professor)).size

  return {
    totalSessions,
    totalEnrolled,
    totalCapacity,
    enrollmentRate,
    uniqueCourses,
    uniqueProfessors,
  }
}

